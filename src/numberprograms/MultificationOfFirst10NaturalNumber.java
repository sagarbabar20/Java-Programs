package numberprograms;

public class MultificationOfFirst10NaturalNumber 
{
	public static void main(String[] args) 
	{
//	 Ex.12 java program to find mulitification of first 10 natural no.
		int multi=10;
		for(int i=1; i<=10; i++)
		{
			multi=multi*i;
		}
		System.out.println(multi);
	}

}
