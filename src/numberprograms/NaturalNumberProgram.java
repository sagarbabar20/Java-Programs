package numberprograms;

public class NaturalNumberProgram 
{
	public static void main(String[] args)
	{
//		Ex.11 java program to find sum of first 10 natural no.
		int sum=0;
		for(int i=1; i<=10;i++)
		{
			sum=sum+i;
		}
		System.out.println("sum of first 10 natural no is -" +sum);

		
	}

}
