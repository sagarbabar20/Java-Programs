package starpatternprograms;

public class StarpatternPrograms2 
{
	public static void main(String[] args)
	{
		
//      Ex.1
		for (int i=1; i<=5; i++)
		{
			for (int j=2; j<=i; j++)
			{
				System.out.print(" ");
			}
			System.out.println("*");
		}
		
//------------------------------------------------------		
//		Ex.2
		for(int k=1; k<=5; k++)
		{
			for(int l=4; l>=k; l--)
			{
				System.out.print(" ");
			}
			System.out.println("*");
		}
		
		

	}
	}


	

