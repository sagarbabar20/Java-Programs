package stringprogram;

public class SeperateIndividualCharacters 
{
//JAVA PROGRAM TO SEPERATE THE INDIVIDUAL CHARACTERS FROM A STRING.
	public static void main(String[] args) 
	{
	 String a = "SAGARBABAR";
    
   for (int i=0; i<a.length(); i++)  
{
	System.out.println(a.charAt(i));
}
	}
}
