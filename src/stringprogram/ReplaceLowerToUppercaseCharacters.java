package stringprogram;

public class ReplaceLowerToUppercaseCharacters 
{
//JAVA PROGRAM TO REPLACE LOWER CASE CHARACTERS WITH UPPER CASE AND VICE VERSA	
	public static void main(String[] args) 
	{
		String a="Sagar Babar";
		
		String x=a.toLowerCase();
		
		System.out.println("Lower case character-"+x);

		String y = a.toUpperCase();
		System.out.println("upper case character-"+y);
		}
	}
